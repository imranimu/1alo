                                       <p>We wish we could assist you with this, but  Drive safe Driving School did not create the website for the Impact Texas Teen Drivers Program. This initiative is under the purview of the Texas DPS, who also created the website. They have the most in-depth understanding of how their particular website functions. They have an email address: ITD@dps.texas.gov.</p>

                                       <p><b>What is the Teen Drivers Impact Texas Program?</b></p>

                                       <p>The Texas DPS offers a free, two-hour online video training called Impact Texas Teen Drivers (ITTD). Any pupil who has successfully completed Texas Parent Taught Drivers Ed or Instructor Taught Drivers Ed and has been issued a Certificate of Completion by  Drive safe Driving School is required to complete ITTD no later than 90 days prior to their driving test.</p>

                                       <p>ITTD is a must. After completing all 12 levels (32 hours) of your  Drive safe Driving School course, you may begin working on it. Eight 15-minute films, totalling two hours, make up ITTD. You have the option of watching all eight courses at once or picking and choosing when to view each one.</p>

                                       <p><b>What if my ITTD certificate was lost?</b></p>

                                       <p>You may download it once more by logging into your ITTD course.</p>

                                       <p><b>I want to enroll in the Impact Texas Teen Drivers program; how do I do that?</b></p>

                                        <p>Make sure you complete your  Drive safe Driving School course FIRST if you are an  Drive safe Driving School student. You may finish the Impact Texas Drivers program after you have earned your certificate from us. NOTE: Your  Drive safe Driving School login is different from your login for ITTD. On the ITTD website, you will register a new account with your own email and password. They don't have to match what you currently have via  Drive safe Driving School.</p>

                                        <ul>
                                            <li>Access the ITTD website.</li>
                                            <li>Select "Register" from the menu.</li>
                                            <li>Here, you will enter your details.</li>
                                            <li>Type of Driver Education:</li>
                                            <li>You should choose "Parent Taught" and then "116 -  Drive safe Driving School Drivers Ed App for Mobile" if you completed your driving hours under Parent Taught.</li>
                                            <li>Choose "Commercial" and provide your driving school's number if you completed your driving hours there. By selecting "Validate a School, Course Provider, or Instructor License" and entering the name of your school, you may discover school numbers here.</li>
                                            <li>Select "Save" from the menu.</li>
                                            <li>Now is the time to begin!</li>
                                        </ul>

                                        <p><b>How can I get a certificate from Impact Texas Teen Drivers?</b></p>

                                        <p>The DPS will send you a certificate via email after you've finished ITTD. When taking your driving test at the DPS, you must print off this certificate and show it there. You may go into your ITTD account and access your ITTD certificate there if you are unable to obtain it by email.</p>

                                        <p><b>The ITTD Certificate, does it expire?</b></p>

                                        <p>It does really run out after 90 days. Before your ITTD certificate expires, be sure you can take your driving test.  Unfortunately, DPS mandates that you repeat ITTD in order to get a new certificate with a new issue date if you let your ITTD certificate expire.</p>

